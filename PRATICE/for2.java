import java.util.Scanner;
class for2{
	public static void main(String[] args) {
		int no;
		Scanner sc=new Scanner(System.in);
		System.out.println("enter any no:");
		no=sc.nextInt();
		//System.out.println("the table of any number ");
		for(int i=0;i>no;){
			System.out.println(i);
			i--;
		}

	}
}